public class Banana {

    private double x;
    private double y;
    private int classe;

    public Banana() {
    }

    public Banana(double x, double y, int classe) {
        this.x = x;
        this.y = y;
        this.classe = classe;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public int getClasse() {
        return classe;
    }

    public void setClasse(int classe) {
        this.classe = classe;
    }
}
